    private void addbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbuttonActionPerformed
        // TODO add your handling code here:
       // try statement for exception 
       try{
        // name inputs set as string   
        String FirstName = firstname.getText();
        String LastName = lastname.getText();
        
        // placements according to 2D arrays
        studentnames[unknown] = FirstName + " " + LastName;
        studentgrades[unknown][0] = Double.parseDouble(test1.getText());
        studentgrades[unknown][1] = Double.parseDouble(test2.getText());
        studentgrades[unknown][2] = Double.parseDouble(test3.getText());
        studentgrades[unknown][3] = Double.parseDouble(test4.getText());
        
       
        
        ace += studentnames[unknown] + " : " 
                    + studentgrades[unknown][0] + "%, "
                    + studentgrades[unknown][1] + "%, " 
                    + studentgrades[unknown][2] + "%, " 
                    + studentgrades[unknown][3] + "% \n";
        //repeats ^^
        unknown++;
        System.out.println(unknown);
       
       }// catch statement to catch invalid input
        catch(NumberFormatException f){
           output.setText("Please make sure your input is valid");
         // cannot input more than 15 student records  
        }catch(ArrayIndexOutOfBoundsException f2){
            output.setText("Maximum number of inputs reached");
       }
       
       
 
    }//GEN-LAST:event_addbuttonActionPerformed

