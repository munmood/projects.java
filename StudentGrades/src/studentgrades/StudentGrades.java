    private void studentavgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_studentavgActionPerformed
        // TODO add your handling code here:
        // string cleared
        String Output = "";
        
            // checks to see if input matches stored input
            for (int row = 0; row < unknown; row++){
                if((firstname.getText() + " " + lastname.getText()).equals(studentnames[row])){
                    // average of four tests
                    double finalscore = (studentgrades[row][0] + studentgrades[row][1] + studentgrades[row][2] + studentgrades[row][3])/4;
                    // outputs name and average
                    Output += studentnames[row] + " : " + finalscore + "% \n";
                    output.setText(Output);
                    
                }
                // otherwise output an error
                else{
                    output.setText("ERROR! Name was not found.");
                }
            }
        
    }//GEN-LAST:event_studentavgActionPerformed

    private void courseavgActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_courseavgActionPerformed
        // TODO add your handling code here:
        
        double test1Average = 0;
        double test2Average = 0;
        double test3Average = 0;
        double test4Average = 0;
        
        // add all grades into their respective doubles
        for (int row=0; row < unknown; row++){
             test1Average += studentgrades[row][0];
        }
        for (int row=0; row < unknown; row++){
             test2Average += studentgrades[row][1];
        }
        for (int row=0; row < unknown; row++){
             test3Average += studentgrades[row][2];
        }
        for (int row=0; row < unknown; row++){
             test4Average += studentgrades[row][3];
        }
        
        // divide all tests by the number of students
        test1Average /= unknown;
        test2Average /= unknown;
        test3Average /= unknown;
        test4Average /= unknown;
        
        // output
        zero = "Test 1 Average: " + test1Average + "% \n" +
                "Test 2 Average: " + test2Average + "% \n" +
                "Test 3 Average: " + test3Average + "% \n" + 
                "Test 4 Average: " + test4Average + "% ";
        output.setText(ace + zero);
        
    }//GEN-LAST:event_courseavgActionPerformed

